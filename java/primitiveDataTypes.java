public class primitiveDataTypes {
    public static void main(String[] args) {
        int a=10;
        float f=1.5f;
        double d=111.23;
        char ch='a';
        boolean b=true;
        System.out.println(a);
        System.out.println(b);
        System.out.println(f);
        System.out.println(d);
        System.out.println(ch);    }
}
